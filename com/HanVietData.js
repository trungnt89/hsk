// Hàm chuyển đổi từ chữ Hán (có thể chứa nhiều chữ) thành danh sách Hán Việt tương ứng
window.getHanVietForWord = function(word) {
  if (!word) return "";
  const chars = Array.from(word);
  const result = [];
  for (const c of chars) {
    if (window.hanVietData && window.hanVietData[c]) {
      // Lấy phiên âm đầu tiên hoặc giữ nguyên nếu ngắn gọn
      result.push(window.hanVietData[c]);
    } else {
      // Nếu không tìm thấy, bỏ qua hoặc có thể giữ ký tự gốc
      result.push("");
    }
  }
  // Nếu toàn dấu "?" thì có thể xem như chưa có data đầy đủ cho chữ này
  if (result.every(x => x === "")) {
    return "";
  }
  return result.join(" ");
};
